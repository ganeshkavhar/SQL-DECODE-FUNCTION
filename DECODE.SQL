SELECT 
EMPLOYEE_ID,FIRST_NAME,LAST_NAME,SALARY,HIRE_DATE
FROM EMPLOYEES
ORDER BY DECODE('F',first_name,'L',last_name,'S',salary,'H',HIRE_DATE);

--JOIN IN DECODES
SELECT 
EMPLOYEE_ID,FIRST_NAME,LAST_NAME,SALARY,HIRE_DATE,DEPARTMENT_NAME
FROM EMPLOYEES E
JOIN DEPARTMENTS D
USING(DEPARTMENT_ID)
ORDER BY DECODE('F',first_name,'L',last_name,'S',salary,'H',HIRE_DATE,'D',DEPARTMENT_NAME); 

--JOIN IN DECODES
SELECT 
EMPLOYEE_ID,FIRST_NAME,LAST_NAME,SALARY,HIRE_DATE,DEPARTMENT_NAME,JOB_TITLE
FROM EMPLOYEES E
JOIN DEPARTMENTS D
USING(DEPARTMENT_ID)
JOIN JOBS J
ON E.JOB_ID=J.JOB_ID
ORDER BY DECODE('F',first_name,'L',last_name,'S',salary,'H',HIRE_DATE,'D',DEPARTMENT_NAME,'J',JOB_TITLE); 

